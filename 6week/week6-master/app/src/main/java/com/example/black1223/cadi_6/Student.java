package com.example.black1223.cadi_6;

public class Student {
     private String Name;
     private String Age;

     public Student(String name,String age) {
         this.Name = name;
         this.Age = age;
     }

    public String getStudentname() {

        return this.Name;
    }
    public String getStudentage() {

        return this.Age;
    }
 }
